# demonstrate arithmetic operations using different data types 

#addition
print('addition----------------')
print(5 + 3) #integer addition
print(5 + 3.5) #integer and float addition


#subtraction
print('subtraction----------------')
print(5 - 3) #integer subtraction
print(5 - 3.5) #integer and float subtraction


#multiplication
print('multiplication----------------')
print(5 * 3) #integer multiplication
print(5 * 3.5) #integer and float multiplication


#division
print('division----------')
print(5 / 3) #integer division
print(5 / 3.5) #integer and float division


#exponentiation
print('exponentiation----------')
print(5 ** 3) #integer exponentiation
print(5 ** 3.5) #integer and float exponentiation


#modulus
print('modulus----------')
print(5 % 3) #integer modulus
print(5 % 3.5) #integer and float modulus
